package com.quantumsoft.insurance.enums;

public enum PolicyStatus {
    ACTIVE,
    PENDING,
    CANCELLED,
    EXPIRED
}

/*Active – The policy is currently valid and in force.

Expired – The policy has passed its end date without renewal.

Pending – The policy is created but awaiting payment or approval.

Cancelled – The policy was terminated either by the insurer or the insured before its expiry.*/

